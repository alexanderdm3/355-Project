package com.example.android.outdoorapp.Utilities;

public class NearbyAirport {
	private String apStationCity;
	private String apStationState;
	private String apStationCountry;
	private String apStationICAO;
	private String apStationLat;
	private String apStationLong;
	
	
	public String getApStationCity() {
		return apStationCity;
	}
	public void setApStationCity(String apStationCity) {
		this.apStationCity = apStationCity;
	}
	public String getApStationState() {
		return apStationState;
	}
	public void setApStationState(String apStationState) {
		this.apStationState = apStationState;
	}
	public String getApStationCountry() {
		return apStationCountry;
	}
	public void setApStationCountry(String apStationCountry) {
		this.apStationCountry = apStationCountry;
	}
	public String getApStationICAO() {
		return apStationICAO;
	}
	public void setApStationICAO(String apStationICAO) {
		this.apStationICAO = apStationICAO;
	}
	public String getApStationLat() {
		return apStationLat;
	}
	public void setApStationLat(String apStationLat) {
		this.apStationLat = apStationLat;
	}
	public String getApStationLong() {
		return apStationLong;
	}
	public void setApStationLong(String apStationLong) {
		this.apStationLong = apStationLong;
	}
	
	
}
